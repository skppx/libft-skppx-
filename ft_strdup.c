#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*copy;
	int		srclen;
	int		i;

	srclen = ft_strlen(src);
	copy = malloc(sizeof(char) * (srclen + 1));
	if (copy == NULL)
		return (NULL);
	i = 0;
	while (src[i])
	{
		copy[i] = src[i];
		i++;
	}
	copy[i] = 0;
	return (copy);
}
